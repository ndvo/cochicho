
  <section class="help">
      <em>Help: learn how to get the most of it.</em>
      <details>
        <summary>Three passwords</summary>
        <p>We ask for three passwords because it is easier to remember three
        distinct passwords than a single large one. The larger the password,
        the lesser the need to have special chars.</p>
        </details>
      <details>
        <summary>Email confirmation</summary>
        <p>We ask you to confirm your email because the email address you provide is the only way we can reach you in case you loose your password. If a user has a typo in his email address we'd best find it out right away.</p>
      </details>
      <details>
        <summary>Recovering password</summary>
        <p>If you loose your password, you can create a new one (three new ones!) from an email we send. <strong>However</strong>, we cannot recover your messages, since they are encrypted with your password. <strong>Fortunately</strong> there is a way to recover your messages: we may ask the sender to resend them. We do this automatically. If you change your password, the senders of your messages will be invited to use their passwords to decrypt the messages and send them again.</p>
        <p>As you can see, if you loose your password you will need to count on your friends to retrieve your messages for you. This is important because we simply cannot recover them. That's why we're safe.</p>
      </details>
      <details>
        <summary>Encrypted messages</summary>
        <p>Every message in this system is encrypted and then stored. This prevents information leaking and provides a secure method of sharing information with your friends. Even if our entire database, together with our source code leaks, your messages would still be safe.</p>
      </details>
  </section>

